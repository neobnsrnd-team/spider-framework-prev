-- =============================================================
-- CMS 이미지 승인(에셋) E2E 테스트 데이터 (Issue #53)
-- 상태별 샘플: WORK / PENDING / APPROVED / REJECTED
-- cmsUser01이 업로더, cmsAdmin01이 결재자 시나리오
-- =============================================================
--
-- SPW_CMS_ASSET 테이블은 Springware-CMS 프로젝트의 DDL(V4~V6)에서 관리하며
-- admin 레포는 해당 테이블 스키마를 소유하지 않는다.
-- 다만 admin E2E Docker 컨테이너는 독립 실행되므로, 테스트가 참조하는 테이블을
-- 여기서 스텁(stub)으로 생성해둔다. REJECTED_REASON 컬럼은 admin 승인 워크플로에서
-- 사용하므로 포함해둔다 (운영 DB 반영은 Springware-CMS 측 마이그레이션으로 별도 수행).
--
-- 참고: Springware-CMS 프로젝트
--      src/db/ddl/V4__asset_and_page_html.sql
--      src/db/ddl/V5__asset_blob_to_file.sql
--      src/db/ddl/V6__page_type_and_asset_state.sql
-- =============================================================

CREATE TABLE SPW_CMS_ASSET (
    ASSET_ID            VARCHAR2(100)   NOT NULL,
    ASSET_NAME          VARCHAR2(200)   NOT NULL,
    BUSINESS_CATEGORY   VARCHAR2(50),
    MIME_TYPE           VARCHAR2(100)   NOT NULL,
    FILE_SIZE           NUMBER,
    ASSET_PATH          VARCHAR2(500),
    ASSET_URL           VARCHAR2(500),
    ASSET_DESC          VARCHAR2(1000),
    ASSET_STATE         VARCHAR2(20)    DEFAULT 'WORK' NOT NULL,
    -- 한글(UTF-8 3바이트) 포함 시 BYTE 단위로 해석되면 ORA-12899 발생 가능 → CHAR 단위 명시
    REJECTED_REASON     VARCHAR2(1000 CHAR),
    USE_YN              CHAR(1)         DEFAULT 'Y' NOT NULL,
    CREATE_USER_ID      VARCHAR2(100),
    CREATE_USER_NAME    VARCHAR2(200),
    LAST_MODIFIER_ID    VARCHAR2(100),
    LAST_MODIFIER_NAME  VARCHAR2(200),
    CREATE_DATE         TIMESTAMP(6)    DEFAULT SYSTIMESTAMP,
    LAST_MODIFIED_DTIME TIMESTAMP(6)    DEFAULT SYSTIMESTAMP,
    CONSTRAINT PK_SPW_CMS_ASSET       PRIMARY KEY (ASSET_ID),
    CONSTRAINT CHK_SPW_ASSET_USE_YN   CHECK (USE_YN IN ('Y','N')),
    CONSTRAINT CHK_SPW_ASSET_STATE    CHECK (ASSET_STATE IN ('WORK','PENDING','APPROVED','REJECTED'))
);

CREATE INDEX IDX_SPW_ASSET_CATEGORY ON SPW_CMS_ASSET (BUSINESS_CATEGORY, USE_YN);
CREATE INDEX IDX_SPW_ASSET_STATE    ON SPW_CMS_ASSET (ASSET_STATE, USE_YN);

